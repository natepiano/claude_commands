#!/bin/bash
# Commit clean-fix style history + report in ~/rust/nate_style. The commit is
# pathspec-scoped to clean-fix's own outputs, so any other dirty, staged, or
# untracked file in the repo (e.g. a hand-edited style guide) is left untouched
# and never blocks the commit.
#
# Committed paths (pathspec-scoped; nothing else is touched):
#   - .history/<project>.jsonl (worktree-modified; append-only, 0 deletions,
#     at most MAX_HISTORY_APPENDS insertions — catch-up commits after skipped
#     cycles are expected and legal)
#   - style_report.md          (worktree-modified; total changed lines <= 200)
#   - CLEAN_FIX_COMMIT_SKIPPED.md staged deletion, only for one-time untracking
#
# .history/.failures/ (untracked retry-diagnostic dumps) is always ignored.
#
# Size guard: if a committed path exceeds its diff-size limit (history must be
# append-only and within MAX_HISTORY_APPENDS lines; report <= 200 changed
# lines), write CLEAN_FIX_COMMIT_SKIPPED.md at the repo root (untracked)
# describing the violation, then exit 0 without committing. The caller runs
# this at the end of every style-fix pass — including passes with failures,
# since finalize-failure rows are history data like any other.
#
# Usage: commit-style-results.sh [--dry-run]
#   --dry-run   Print the plan and `git diff --stat` instead of committing.
#
# Environment:
#   NATE_STYLE_DIR   Override target repo (default: $HOME/rust/nate_style).

set -euo pipefail

NATE_STYLE_DIR="${NATE_STYLE_DIR:-$HOME/rust/nate_style}"
SENTINEL_NAME="CLEAN_FIX_COMMIT_SKIPPED.md"
SENTINEL="$NATE_STYLE_DIR/$SENTINEL_NAME"
MAX_REPORT_LINES=200
# History files are append-only JSONL: one row per finalized run. More than one
# new row per file is normal whenever a previous cycle's commit was skipped
# (failures, crash mid-run) — the next commit catches up. The cap exists only
# to catch a runaway loop appending rows every cycle.
MAX_HISTORY_APPENDS=20
COMMIT_MSG="chore(style): clean-fix style history + report"

DRY_RUN=false
for arg in "$@"; do
    case "$arg" in
        --dry-run) DRY_RUN=true ;;
        -h|--help)
            sed -n '2,21p' "$0"
            exit 0
            ;;
        *)
            echo "commit-style-results: unknown argument: $arg" >&2
            exit 2
            ;;
    esac
done

if [[ ! -d "$NATE_STYLE_DIR/.git" ]]; then
    echo "commit-style-results: $NATE_STYLE_DIR is not a git repository" >&2
    exit 2
fi

cd "$NATE_STYLE_DIR"

sentinel_exclude_added=false

sentinel_is_excluded() {
    local exclude_file
    exclude_file="$(git rev-parse --git-path info/exclude)"
    [[ -f "$exclude_file" ]] && grep -qxF "$SENTINEL_NAME" "$exclude_file"
}

ensure_sentinel_excluded() {
    local exclude_file
    exclude_file="$(git rev-parse --git-path info/exclude)"
    if sentinel_is_excluded; then
        return
    fi
    if $DRY_RUN; then
        sentinel_exclude_added=true
        return
    fi
    mkdir -p "$(dirname "$exclude_file")"
    touch "$exclude_file"
    printf '%s\n' "$SENTINEL_NAME" >>"$exclude_file"
}

ensure_sentinel_excluded

if git ls-files --error-unmatch "$SENTINEL_NAME" >/dev/null 2>&1; then
    echo "commit-style-results: skipped — $SENTINEL_NAME is tracked; run exclude in $NATE_STYLE_DIR first"
    exit 0
fi

# Remove any prior sentinel so it does not contaminate the "other files" check
# and does not linger across runs. In dry-run mode, temporarily hide it instead
# so the script stays side-effect-free.
if ! $DRY_RUN; then
    rm -f "$SENTINEL"
fi

write_sentinel() {
    local reason="$1"
    local details="$2"
    local body
    body="$(cat <<EOF
# Clean-fix style commit skipped — $(date -u +%Y-%m-%dT%H:%M:%SZ)

**Reason:** $reason

## Details

$details

---
Generated by \`commit-style-results.sh\`. This file is untracked; resolve the
listed issues (commit, stash, or discard as appropriate) and the next clean-fix
run will retry.
EOF
)"
    if $DRY_RUN; then
        echo "commit-style-results: DRY RUN — would write $SENTINEL:"
        echo "---"
        printf '%s\n' "$body"
        echo "---"
    else
        printf '%s\n' "$body" >"$SENTINEL"
    fi
}

# --- Classify working tree ---------------------------------------------------
porcelain="$(git status --porcelain)"

if [[ -z "$porcelain" ]]; then
    echo "commit-style-results: nothing to commit"
    exit 0
fi

unexpected=()
history_files=()
has_report=false
has_sentinel_removal=false

while IFS= read -r line; do
    [[ -z "$line" ]] && continue
    status="${line:0:2}"
    path="${line:3}"
    if [[ "$path" == "$SENTINEL_NAME" ]]; then
        if [[ "$status" == "D " ]]; then
            has_sentinel_removal=true
            continue
        fi
        if $DRY_RUN && $sentinel_exclude_added && [[ "$status" == "??" ]]; then
            continue
        fi
    fi
    # `.history/.failures/` holds per-retry diagnostic dumps written by the
    # style-eval retry path. Local-only, never committed — skip without flagging.
    case "$path" in
        .history/.failures/|.history/.failures/*) continue ;;
    esac
    # Only accept worktree-only modifications ( M) of allowed paths.
    if [[ "$status" != " M" ]]; then
        unexpected+=("$line")
        continue
    fi
    case "$path" in
        style_report.md) has_report=true ;;
        .history/*.jsonl) history_files+=("$path") ;;
        *) unexpected+=("$line") ;;
    esac
done <<<"$porcelain"

# Unrelated working-tree changes no longer block the commit: the commit below
# is pathspec-scoped to clean-fix's own outputs, so nothing else can be swept
# in. Report what is being left alone so it stays visible in the run log.
if (( ${#unexpected[@]} > 0 )); then
    echo "commit-style-results: leaving ${#unexpected[@]} unrelated working-tree change(s) untouched:"
    printf '  %s\n' "${unexpected[@]}"
fi

# --- Size checks -------------------------------------------------------------
violations=()
numstat_rows=()

while IFS=$'\t' read -r added deleted path; do
    [[ -z "$path" ]] && continue
    numstat_rows+=("$(printf '%s\t%s\t%s' "$added" "$deleted" "$path")")
    case "$path" in
        .history/*.jsonl)
            if [[ "$deleted" != "0" ]]; then
                violations+=("$path: +$added -$deleted (history is append-only; deletions mean rows were rewritten or truncated)")
            elif (( added > MAX_HISTORY_APPENDS )); then
                violations+=("$path: +$added -$deleted (exceeds $MAX_HISTORY_APPENDS appended rows; suggests a runaway append loop)")
            fi
            ;;
        style_report.md)
            total=$(( added + deleted ))
            if (( total > MAX_REPORT_LINES )); then
                violations+=("style_report.md: +$added -$deleted = $total lines changed (max $MAX_REPORT_LINES)")
            fi
            ;;
    esac
done < <(git diff --numstat -- style_report.md .history/)

if (( ${#violations[@]} > 0 )); then
    details=$'Diff-size checks failed:\n\n'
    for v in "${violations[@]}"; do
        details+="- $v"$'\n'
    done
    details+=$'\n## Numstat (added\\tdeleted\\tpath)\n\n```\n'
    details+="$(printf '%s\n' "${numstat_rows[@]}")"
    details+=$'\n```'
    write_sentinel "diff size limits exceeded" "$details"
    echo "commit-style-results: skipped — diff size limits (sentinel written)"
    exit 0
fi

# --- Commit ------------------------------------------------------------------
# macOS bash 3.2 + `set -u`: expanding an empty array with "${arr[@]}" is an
# unbound-variable error, so every expansion below is guarded by a length check.
paths_to_add=()
$has_report && paths_to_add+=("style_report.md")
if (( ${#history_files[@]} > 0 )); then
    for f in "${history_files[@]}"; do
        paths_to_add+=("$f")
    done
fi

if (( ${#paths_to_add[@]} == 0 )) && ! $has_sentinel_removal; then
    echo "commit-style-results: nothing of ours to commit (only unrelated changes present)"
    exit 0
fi

if $DRY_RUN; then
    echo "commit-style-results: DRY RUN"
    echo "  repo:           $NATE_STYLE_DIR"
    echo "  commit message: $COMMIT_MSG"
    if $sentinel_exclude_added; then
        echo "  would exclude:"
        echo "    $SENTINEL_NAME"
    fi
    echo "  would stage:"
    if (( ${#paths_to_add[@]} > 0 )); then
        printf '    %s\n' "${paths_to_add[@]}"
    else
        echo "    (none)"
    fi
    if $has_sentinel_removal; then
        echo "  would keep staged removal:"
        echo "    $SENTINEL_NAME"
    fi
    echo ""
    if (( ${#paths_to_add[@]} > 0 )); then
        git diff --stat -- "${paths_to_add[@]}"
    fi
    exit 0
fi

# Scope the commit by pathspec to clean-fix's own outputs. A partial commit
# (`git commit -- <paths>`) records only those paths from the working tree and
# leaves every other dirty, staged, or untracked file exactly as-is — there is
# no path by which an unrelated edit can be swept into this commit.
commit_paths=()
if (( ${#paths_to_add[@]} > 0 )); then
    commit_paths=("${paths_to_add[@]}")
fi
$has_sentinel_removal && commit_paths+=("$SENTINEL_NAME")

git commit -m "$COMMIT_MSG" -- "${commit_paths[@]}"
committed_count=${#paths_to_add[@]}
$has_sentinel_removal && committed_count=$((committed_count + 1))
echo "commit-style-results: committed $committed_count file(s)"
