#!/bin/bash
# Commit nightly style history + report in ~/rust/nate_style when the working
# tree contains only the expected nightly outputs. Otherwise leave a sentinel
# markdown file explaining why and exit 0.
#
# Expected dirty files (nothing else may be dirty, staged, or untracked):
#   - .history/<project>.jsonl (worktree-modified; each +1 insertion, 0 deletions)
#   - style_report.md          (worktree-modified; total changed lines <= 200)
#
# On any violation: write NIGHTLY_COMMIT_SKIPPED.md at the repo root (untracked)
# describing what blocked the commit, then exit 0. The caller is expected to
# run this only when the upstream style-fix pass had zero failures.
#
# Usage: commit-style-results.sh [--dry-run]
#   --dry-run   Print the plan and `git diff --stat` instead of committing.
#
# Environment:
#   NATE_STYLE_DIR   Override target repo (default: $HOME/rust/nate_style).

set -euo pipefail

NATE_STYLE_DIR="${NATE_STYLE_DIR:-$HOME/rust/nate_style}"
SENTINEL_NAME="NIGHTLY_COMMIT_SKIPPED.md"
SENTINEL="$NATE_STYLE_DIR/$SENTINEL_NAME"
MAX_REPORT_LINES=200
COMMIT_MSG="chore(style): nightly style history + report"

DRY_RUN=false
for arg in "$@"; do
    case "$arg" in
        --dry-run) DRY_RUN=true ;;
        -h|--help)
            sed -n '2,21p' "$0"
            exit 0
            ;;
        *)
            echo "commit-style-results: unknown argument: $arg" >&2
            exit 2
            ;;
    esac
done

if [[ ! -d "$NATE_STYLE_DIR/.git" ]]; then
    echo "commit-style-results: $NATE_STYLE_DIR is not a git repository" >&2
    exit 2
fi

cd "$NATE_STYLE_DIR"

# Remove any prior sentinel so it does not contaminate the "other files" check
# and does not linger across runs. In dry-run mode, temporarily hide it instead
# so the script stays side-effect-free.
if $DRY_RUN; then
    if [[ -f "$SENTINEL" ]]; then
        SENTINEL_BACKUP="$(mktemp)"
        mv "$SENTINEL" "$SENTINEL_BACKUP"
        trap 'mv "$SENTINEL_BACKUP" "$SENTINEL"' EXIT
    fi
else
    rm -f "$SENTINEL"
fi

write_sentinel() {
    local reason="$1"
    local details="$2"
    local body
    body="$(cat <<EOF
# Nightly style commit skipped — $(date -u +%Y-%m-%dT%H:%M:%SZ)

**Reason:** $reason

## Details

$details

---
Generated by \`commit-style-results.sh\`. This file is untracked; resolve the
listed issues (commit, stash, or discard as appropriate) and the next nightly
run will retry.
EOF
)"
    if $DRY_RUN; then
        echo "commit-style-results: DRY RUN — would write $SENTINEL:"
        echo "---"
        printf '%s\n' "$body"
        echo "---"
    else
        printf '%s\n' "$body" >"$SENTINEL"
    fi
}

# --- Classify working tree ---------------------------------------------------
porcelain="$(git status --porcelain)"

if [[ -z "$porcelain" ]]; then
    echo "commit-style-results: nothing to commit"
    exit 0
fi

unexpected=()
history_files=()
has_report=false

while IFS= read -r line; do
    [[ -z "$line" ]] && continue
    status="${line:0:2}"
    path="${line:3}"
    # Only accept worktree-only modifications ( M) of allowed paths.
    if [[ "$status" != " M" ]]; then
        unexpected+=("$line")
        continue
    fi
    case "$path" in
        style_report.md) has_report=true ;;
        .history/*.jsonl) history_files+=("$path") ;;
        *) unexpected+=("$line") ;;
    esac
done <<<"$porcelain"

if (( ${#unexpected[@]} > 0 )); then
    details=$'Unexpected working-tree entries (only worktree-modified `.history/*.jsonl` and `style_report.md` are permitted):\n\n```\n'
    details+="$(printf '%s\n' "${unexpected[@]}")"
    details+=$'\n```'
    write_sentinel "unexpected working-tree changes" "$details"
    echo "commit-style-results: skipped — unexpected changes (sentinel written)"
    exit 0
fi

# --- Size checks -------------------------------------------------------------
violations=()
numstat_rows=()

while IFS=$'\t' read -r added deleted path; do
    [[ -z "$path" ]] && continue
    numstat_rows+=("$(printf '%s\t%s\t%s' "$added" "$deleted" "$path")")
    case "$path" in
        .history/*.jsonl)
            if [[ "$added" != "1" || "$deleted" != "0" ]]; then
                violations+=("$path: +$added -$deleted (expected +1 -0; multi-line diffs suggest accumulation across nights)")
            fi
            ;;
        style_report.md)
            total=$(( added + deleted ))
            if (( total > MAX_REPORT_LINES )); then
                violations+=("style_report.md: +$added -$deleted = $total lines changed (max $MAX_REPORT_LINES)")
            fi
            ;;
    esac
done < <(git diff --numstat -- style_report.md .history/)

if (( ${#violations[@]} > 0 )); then
    details=$'Diff-size checks failed:\n\n'
    for v in "${violations[@]}"; do
        details+="- $v"$'\n'
    done
    details+=$'\n## Numstat (added\\tdeleted\\tpath)\n\n```\n'
    details+="$(printf '%s\n' "${numstat_rows[@]}")"
    details+=$'\n```'
    write_sentinel "diff size limits exceeded" "$details"
    echo "commit-style-results: skipped — diff size limits (sentinel written)"
    exit 0
fi

# --- Commit ------------------------------------------------------------------
paths_to_add=()
$has_report && paths_to_add+=("style_report.md")
for f in "${history_files[@]}"; do
    paths_to_add+=("$f")
done

if $DRY_RUN; then
    echo "commit-style-results: DRY RUN"
    echo "  repo:           $NATE_STYLE_DIR"
    echo "  commit message: $COMMIT_MSG"
    echo "  would stage:"
    printf '    %s\n' "${paths_to_add[@]}"
    echo ""
    git diff --stat -- "${paths_to_add[@]}"
    exit 0
fi

git add -- "${paths_to_add[@]}"
git commit -m "$COMMIT_MSG"
echo "commit-style-results: committed ${#paths_to_add[@]} file(s)"
